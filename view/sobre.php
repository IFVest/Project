    <p> O IFPR se destaca por ser uma instituição pública de ensino que oferece formação gratuita aos estudantes. 
        Além disso, também é conhecido por explorar o aprendizado prático com a vivência de experiências baseadas na
         realidade de atuação profissional das diferentes áreas. 
    </p> 
    
    <p> Os Institutos Federais são instituições pluricurriculares e multicampi (reitoria, campus, campus avançado, 
        polos de inovação e polos de educação a distância), especializados na oferta de educação profissional e 
        tecnológica (EPT) em todos os seus níveis e formas de articulação com os demais níveis e modalidades da 
        Educação Nacional, oferta diferentes tipos de cursos de EPT, além de licenciaturas, bacharelados e 
        pós-graduação stricto sensu. Instituídos no momento de constituição da Rede Federal, os institutos têm como 
        obrigatoriedade legal garantir um mínimo de 50% de suas vagas para a oferta de cursos técnicos de nível médio,
         prioritariamente na forma integrada. Devem ainda garantir o mínimo de 20% de suas vagas para atender a oferta
          de cursos de licenciatura, bem como programas especiais de formação pedagógica, com vistas a formação de 
          professores para a educação básica, sobretudo nas áreas de ciências e matemática, e para a educação
           profissional. Destaca-se também sua atribuição no desenvolvimento de soluções técnicas e tecnológicas
            por meio de pesquisas aplicadas e as ações de extensão junto à comunidade com vistas ao avanço econômico 
            e social local e regional. 
    </p> 
    
